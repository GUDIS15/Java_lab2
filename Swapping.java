package LAB2;

public class Swapping {
	public static void main(String[] args) {
		float a=3.40f;
		float b=1.11f;
		System.out.println("Frst no:"+a);
		System.out.println("Sec no:"+b);
		
		float temp=a;//a val is stored in temp
		a=b;//b val is stored in a
		b=temp;//so 11=10
		System.out.println("Frst no:"+a);
		System.out.println("Sec no:"+b);	
		
		
	}

}
